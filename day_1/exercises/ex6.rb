# Don't y'all know that explaining the joke ruins the joke?
# This is a binary joke
types_of_people = 10
# This is a joke I've heard before, like a decade ago. Also, an example of a string inside of a string
x = "There are #{types_of_people} types of people."
# get ready for a joke that's funniness is either I or O!
binary = "binary"
# contractions, nice
do_not = "don't"
# An instance of a string inside of a string
y = "Those who know #{binary} and those who #{do_not}."
# Hey, so, these are self-explanatory:
puts x
puts y
# This is a string inside of a string
puts "I said: #{x}."
# This is also a string inside of a string
puts "I also said: '#{y}'."
# Like most jokes not made by me:
hilarious = false
# I almost thought that was a case of a string inside of a string, but it isn't because it's using a boolean
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"
# Get ready...
puts joke_evaluation
# Ah, the classic--
w = "This is the left side of..."
# misdirect.
e = "a string with a right side."
# Weeeeeeee it's two strings inside of a longer string!
puts w + e
# Adding two strings together (that aren't numerical values) causes them to be placed in order!
